export const userLogout = async (formData: FormData) => {

    // Remove all Cookies
    cookies().set('session-id', '');
    cookies().set('user-id', '');

    // Redirect to Home page
    redirect(`${process.env.WEBSITE_DOMAIN}/`);
}
